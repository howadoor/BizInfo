namespace Perenis.Core.CommandLine
{
    /// <summary>
    /// Represents a command-line operation.
    /// </summary>
    public interface IValidableCmdOperation : ICmdOperation
    {
        /// <summary>
        /// Validates the operation in terms of its arguments that have been parsed.
        /// </summary>
        /// <returns><c>true</c> when the operation is valid; otherwise, <c>false</c>.</returns>
        bool ValidateOperation();
    }
}